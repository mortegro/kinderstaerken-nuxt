    h4 Fliegerbau
    p Der Papierflieger muss aus einem einzigen Stück Papier (Blattgröße maximal A3) vor Ort gefertigt werden. Die Grammatur der Papiers darf dabei 100 g/m2 nicht überscheiten. Das Papier darf geschnitten werden, abgeschnittene Stücke dürfen jedoch nicht weiter verwendet werden. 
