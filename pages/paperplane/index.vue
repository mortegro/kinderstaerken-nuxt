    p Gestartet wird der Papierflieger durch eine Person (den Werfer), welche ihn ohne Hilfe an einer auf dem Boden markierten Linie (der Startlinie) wirft. Ein Anlauf von 5 m bis zur Startlinie ist erlaubt aber die Benutzung von Rampen oder ähnlicher Hilfsmittel ist untersagt. Der Werfer (einschließlich Füße und Kleidung) darf die Startlinie nicht berühren oder überschreiten.
    p Die Weitenmessung erfolgt in einer geraden Linie senkrecht zur Startlinie. Gemessen wird der Abstand von der Startlinie bis zu der Stelle, an der eine Parallele der Startlinie diese Messlinie schneidet. Maßgeblich ist der Punkt, an welchem der Papierflieger auf oder über dem Boden zur Ruhe kommt.
